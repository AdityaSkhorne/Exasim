function f = getflux(udg,param)
gam = param(1);
gam1 = gam - 1.0;
Re = param(2);
Pr = param(3);
Minf = param(4);
Re1 = 1/Re;
M2 = Minf^2;
c23 = 2.0/3.0;
fc = 1/(gam1*M2*Re*Pr);
r = udg(1);
ru = udg(2);
rv = udg(3);
rE = udg(4);
rx = udg(5);
rux = udg(6);
rvx = udg(7);
rEx = udg(8);
ry = udg(9);
ruy = udg(10);
rvy = udg(11);
rEy = udg(12);
r1 = 1/r;
uv = ru*r1;
vv = rv*r1;
E = rE*r1;
q = 0.5*(uv*uv+vv*vv);
p = gam1*(rE-r*q);
h = E+p*r1;
fi = [ru, ru*uv+p, rv*uv, ru*h, ...
        rv, ru*vv, rv*vv+p, rv*h];
ux = (rux - rx*uv)*r1;
vx = (rvx - rx*vv)*r1;
qx = uv*ux + vv*vx;
px = gam1*(rEx - rx*q - r*qx);
Tx = gam*M2*(px*r - p*rx)*r1^2;
uy = (ruy - ry*uv)*r1;
vy = (rvy - ry*vv)*r1;
qy = uv*uy + vv*vy;
py = gam1*(rEy - ry*q - r*qy);
Ty = gam*M2*(py*r - p*ry)*r1^2;
txx = Re1*c23*(2*ux - vy);
txy = Re1*(uy + vx);
tyy = Re1*c23*(2*vy - ux);
fv = [0, txx, txy, uv*txx + vv*txy + fc*Tx, ...
      0, txy, tyy, uv*txy + vv*tyy + fc*Ty];
f = fi+fv;
