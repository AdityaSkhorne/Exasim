template <typename T> void opuOutput(T *f, T *xdg, T *udg, T *odg, T *wdg, T *uinf, T *param, T time, int modelnumber, int ng, int nc, int ncu, int nd, int ncx, int nco, int ncw, int nce, int npe, int ne, Mutation::Mixture *mix)
{
	double Ustate[6];
	int nspecies = 5;
	int ndim = 2;
	double rho_scale = uinf[0];
	double u_scale = uinf[1];
	double rhoe_scale = uinf[2];
	double omega_scale = rho_scale*u_scale;
    double mu_scale = uinf[4];
    double kappa_scale = uinf[5];
    double Uwork[5];
    double dTdU[6];
	double D_i[5];
    double h_i[5];

	for (int i = 0; i <ng; i++) {
		int j = i%npe;
		int k = (i-j)/npe;
		T param2 = param[1];
		T param22 = param[21];
		T uinf1 = uinf[0];
		T uinf14 = uinf[13];
		T uinf15 = uinf[14];
		T uinf16 = uinf[15];
		T uinf17 = uinf[16];
		T uinf18 = uinf[17];
		T uinf19 = uinf[18];
		T xdg1 = xdg[j+npe*0+npe*ncx*k];
		T xdg2 = xdg[j+npe*1+npe*ncx*k];
		T udg1 = udg[j+npe*0+npe*nc*k];
		T udg2 = udg[j+npe*1+npe*nc*k];
		T udg3 = udg[j+npe*2+npe*nc*k];
		T udg4 = udg[j+npe*3+npe*nc*k];
		T udg5 = udg[j+npe*4+npe*nc*k];
		T udg6 = udg[j+npe*5+npe*nc*k];
		T udg7 = udg[j+npe*6+npe*nc*k];
		T udg8 = udg[j+npe*7+npe*nc*k];
		T udg9 = udg[j+npe*8+npe*nc*k];
		T udg10 = udg[j+npe*9+npe*nc*k];
		T udg11 = udg[j+npe*10+npe*nc*k];
		T udg12 = udg[j+npe*11+npe*nc*k];
		T udg13 = udg[j+npe*12+npe*nc*k];
		T udg14 = udg[j+npe*13+npe*nc*k];
		T udg15 = udg[j+npe*14+npe*nc*k];
		T udg16 = udg[j+npe*15+npe*nc*k];
		T udg17 = udg[j+npe*16+npe*nc*k];
		T udg18 = udg[j+npe*17+npe*nc*k];
		T udg19 = udg[j+npe*18+npe*nc*k];
		T udg20 = udg[j+npe*19+npe*nc*k];
		T udg21 = udg[j+npe*20+npe*nc*k];
		T udg22 = udg[j+npe*21+npe*nc*k];
		T udg23 = udg[j+npe*22+npe*nc*k];
		T udg24 = udg[j+npe*23+npe*nc*k];
		T odg1 = odg[j+npe*0+npe*nco*k];
		T odg2 = odg[j+npe*1+npe*nco*k];

		T t1pi = 1.0/3.141592653589793;
		
		udg1 = udg1*(t1pi*atan(udg1*1.0E+12)+1.0/2.0)+3.182454300088011E-13;
		udg2 = udg2*(t1pi*atan(udg2*1.0E+12)+1.0/2.0)+3.182454300088011E-13;
		udg3 = udg3*(t1pi*atan(udg3*1.0E+12)+1.0/2.0)+3.182454300088011E-13;
		udg4 = udg4*(t1pi*atan(udg4*1.0E+12)+1.0/2.0)+3.182454300088011E-13;
		udg5 = udg5*(t1pi*atan(udg5*1.0E+12)+1.0/2.0)+3.182454300088011E-13;

		double Ucons[8] = {udg1, udg2, udg3, udg4, udg5, udg6, udg7, udg8};
		dimensionalizeConsVars(Ucons, (double*)uinf, nspecies, ndim);
		conservativeToState(Ucons, Ustate, (double*)uinf, nspecies, ndim);
		double rhovec[5] = {Ustate[0],Ustate[1],Ustate[2],Ustate[3],Ustate[4]};
		double rhoe = Ustate[nspecies];

		mix->setState(rhovec, &rhoe, 0);

		uinf1 = mix->P() / rhoe_scale;

		dT_dUstate(dTdU, Ustate, Uwork, nspecies, ndim, mix);
		nondimensionalize_dT_dUstate(dTdU, (double*)uinf, nspecies, nd);
		uinf14 = dTdU[0]; uinf15 = dTdU[1]; uinf16 = dTdU[2]; uinf17 = dTdU[3]; uinf18 = dTdU[4]; uinf19 = dTdU[5];


		T t2 = atan(param22);
		T t3 = param22*udg1;
		T t4 = param22*udg2;
		T t5 = param22*udg3;
		T t6 = param22*udg4;
		T t7 = param22*udg5;
		T t8 = param22*param22;
		T t9 = udg1*udg1;
		T t10 = udg2*udg2;
		T t11 = udg3*udg3;
		T t12 = udg4*udg4;
		T t13 = udg5*udg5;
		T t14 = udg6*udg6;
		T t15 = udg7*udg7;
		T t21 = 1.0/3.141592653589793;
		T t16 = atan(t3);
		T t17 = atan(t4);
		T t18 = atan(t5);
		T t19 = atan(t6);
		T t20 = atan(t7);
		T t22 = t3*t3;
		T t23 = t4*t4;
		T t24 = t5*t5;
		T t25 = t6*t6;
		T t26 = t7*t7;
		T t27 = t2*t21*5.0;
		T t28 = t22+1.0;
		T t29 = t23+1.0;
		T t30 = t24+1.0;
		T t31 = t25+1.0;
		T t32 = t26+1.0;
		T t33 = t16*t21;
		T t34 = t17*t21;
		T t35 = t18*t21;
		T t36 = t19*t21;
		T t37 = t20*t21;
		T t38 = -t27;
		T t39 = 1.0/t28;
		T t40 = 1.0/t29;
		T t41 = 1.0/t30;
		T t42 = 1.0/t31;
		T t43 = 1.0/t32;
		T t44 = t33+1.0/2.0;
		T t45 = t34+1.0/2.0;
		T t46 = t35+1.0/2.0;
		T t47 = t36+1.0/2.0;
		T t48 = t37+1.0/2.0;
		T t49 = t44*udg1;
		T t50 = t45*udg2;
		T t51 = t46*udg3;
		T t52 = t47*udg4;
		T t53 = t48*udg5;
		T t54 = t3*t21*t39;
		T t55 = t4*t21*t40;
		T t56 = t5*t21*t41;
		T t57 = t6*t21*t42;
		T t58 = t7*t21*t43;
		T t59 = t44+t54;
		T t60 = t45+t55;
		T t61 = t46+t56;
		T t62 = t47+t57;
		T t63 = t48+t58;
		T t74 = t38+t49+t50+t51+t52+t53+5.0/2.0;
		T t64 = t59*udg9;
		T t65 = t60*udg10;
		T t66 = t61*udg11;
		T t67 = t59*udg17;
		T t68 = t62*udg12;
		T t69 = t60*udg18;
		T t70 = t63*udg13;
		T t71 = t61*udg19;
		T t72 = t62*udg20;
		T t73 = t63*udg21;
		T t75 = 1.0/t74;
		T t76 = t75*t75;
		T t79 = t64+t65+t66+t68+t70;
		T t80 = t67+t69+t71+t72+t73;
		T t77 = (t14*t76)/2.0;
		T t78 = (t15*t76)/2.0;
		T t81 = t77+t78;
		f[j+npe*0+npe*nce*k] = odg1;
		f[j+npe*1+npe*nce*k] = uinf19*(-udg16+t79*t81+t74*(t76*udg6*(udg14-t75*t79*udg6)+t76*udg7*(udg15-t75*t79*udg7)))-t64*uinf14-t65*uinf15-t66*uinf16-t68*uinf17-t70*uinf18;
		f[j+npe*2+npe*nce*k] = uinf19*(-udg24+t80*t81+t74*(t76*udg6*(udg22-t75*t80*udg6)+t76*udg7*(udg23-t75*t80*udg7)))-t67*uinf14-t69*uinf15-t71*uinf16-t72*uinf17-t73*uinf18;
		f[j+npe*3+npe*nce*k] = uinf1;
		f[j+npe*4+npe*nce*k] = mix->T();
		f[j+npe*5+npe*nce*k] = udg6;
		f[j+npe*6+npe*nce*k] = udg7;
		f[j+npe*7+npe*nce*k] = udg8;
		f[j+npe*8+npe*nce*k] = 0.0;
		f[j+npe*9+npe*nce*k] = 0.0;
	}
}

